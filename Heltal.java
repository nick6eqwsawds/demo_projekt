public class Heltal {
    public static void main(String[] args) throws Exception{
        int tal1 = 2;
        int tal2 = 30;

        int summa_addition = tal1+tal2;
        int summa_subtraktion = tal1-tal2;

        System.out.println("summan av tal + tal2 채r "+summa_addition);
        System.out.println("summan av tal - tal2 채r "+summa_subtraktion);

        int produkt = tal1*tal2;
        int kvot = tal1/tal2;

        System.out.println("Produkten av tal1*tal2 채r "+produkt);
        System.out.println("Kvoten av tal1/tal2 채r "+kvot);
    }
    
}
